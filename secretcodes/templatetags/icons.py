@register.simple_tag
def icon(name, **attrs):
    """Render an inline SVG icon by name.

    ``attrs`` accepts ``class`` (extra CSS classes) and ``label``
    (turns the icon into a labelled image instead of decorative).
    """
    if name not in ICONS:
        raise template.TemplateSyntaxError(f"Unknown icon: {name!r}")
    viewbox, path = ICONS[name]
    extra_class = attrs.get("class", "")
    label = attrs.get("label", "")
    classes = "sc-icon" + ((" " + extra_class) if extra_class else "")
    if label:
        a11y = format_html(' role="img" aria-label="{}"', label)
    else:
        a11y = mark_safe(' aria-hidden="true"')
    return format_html(
        '<svg xmlns="http://www.w3.org/2000/svg" viewBox="{}" '
        'width="1em" height="1em" fill="currentColor" class="{}"{}>'
        '<path d="{}"/></svg>',
        viewbox,
        classes,
        a11y,
        mark_safe(path),
    )
